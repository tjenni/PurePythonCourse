# Computer sind in der Lage Informationen zu speichern. In Python
# verwendet man dazu Variablen. Hier ist ein Beispiel:

number = 4

# Das bedeutet, dass Python eine Variabel mit dem Namen `number` erstellt
# und ihr den Wert 4 zuweist.

# Das Gleichheitszeichen in `number = 4` ist kein mathematisches
# Gleichheitszeichen, sondern ein sogenannter Zuweisungsoperator,
# der Python anweist, der Variablen number den Wert 4 zuzuweisen.



# Python unterscheidet zwischen Gross- und Kleinschreibung,
# daher sind `number` und `Number` zwei verschiedene Variablen.

Number = 8

